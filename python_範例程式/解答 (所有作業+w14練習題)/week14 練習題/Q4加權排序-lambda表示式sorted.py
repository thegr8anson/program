'''
給定一組成績列表 scores 和權重列表 weights，每個成績列表中的元素分別對應一個權重。請計算每組成績的加權平均值，並按照加權平均值的升序對成績進行排序。

範例輸入與輸出
輸入:[80, 90, 100], [70, 85, 95], [88, 92, 76]
第一組成績的加權平均值：
80*0.2+90*0.3+100*0.5 = 92.0
第二組成績的加權平均值：
70*0.2+85*0.3+95*0.5 = 87.5
第三組成績的加權平均值：
88*0.2+92*0.3+76*0.5 = 82.8

根據加權平均數將三組成績做升序的排序，由加權平均值由低到高做排序
輸出:[88, 92, 76], [70, 85, 95], [80, 90, 100]

Hint:
1.使用 sorted 函數排序
sorted(scores, key=...)：
sorted 函數根據 key 提供的值對 scores 列表進行排序。
key=lambda x: ... 表示計算每組成績的加權平均值作為排序依據。
2.zip(x, weights)：將成績列表和權重列表配對
'''

# 定義成績和權重
scores = [[80, 90, 100], [70, 85, 95], [88, 92, 76]]
weights = [0.2, 0.3, 0.5]

# 使用 sorted 和 lambda 表達式進行排序
sorted_scores = sorted(scores, key=lambda x: sum(s * w for s, w in zip(x, weights)))
print(sorted_scores)