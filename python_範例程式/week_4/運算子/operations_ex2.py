'''
複合運算範例
'''

# 1. 加法複合運算子 (+=)
a = 10
print("初始 a:", a)  # a = 10
a += 5  # 相當於 a = a + 5
print("使用加法複合運算子 (a += 5):", a)  # a = 15

# 2. 減法複合運算子 (-=)
b = 20
print("初始 b:", b)  # b = 20
b -= 7  # 相當於 b = b - 7
print("使用減法複合運算子 (b -= 7):", b)  # b = 13

# 3. 乘法複合運算子 (*=)
c = 4
print("初始 c:", c)  # c = 4
c *= 3  # 相當於 c = c * 3
print("使用乘法複合運算子 (c *= 3):", c)  # c = 12

# 4. 除法複合運算子 (/=)
d = 25
print("初始 d:", d)  # d = 25
d /= 5  # 相當於 d = d / 5
print("使用除法複合運算子 (d /= 5):", d)  # d = 5.0 (除法會返回浮點數)

# 5. 取餘數複合運算子 (%=)
e = 10
print("初始 e:", e)  # e = 10
e %= 3  # 相當於 e = e % 3
print("使用取餘數複合運算子 (e %= 3):", e)  # e = 1

# 6. 次方複合運算子 (**=)
f = 2
print("初始 f:", f)  # f = 2
f **= 3  # 相當於 f = f ** 3
print("使用次方複合運算子 (f **= 3):", f)  # f = 8

# 7. 整數除法複合運算子 (//=)
g = 17
print("初始 g:", g)  # g = 17
g //= 3  # 相當於 g = g // 3
print("使用整數除法複合運算子 (g //= 3):", g)  # g = 5